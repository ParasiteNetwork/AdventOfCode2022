using GetGridCallback = std::function<int * (unsigned, unsigned)>;

unsigned find_trees(GetGridCallback const & get)
{
	unsigned visible_trees = 0;
	
	for(unsigned y = 0; y < 99; ++y)
	{
		int tree_height = -1;
		
		unsigned last_blocking_tree_x = 0;
		
		for(unsigned xleft = 0 ; xleft < 99; ++xleft)
		{
			int * tree = get(y, xleft);
			int treeh = std::abs(*tree);
			
			if(treeh > tree_height)
			{
				tree_height = treeh;
				
				if(*tree >= 0)
				{
					++visible_trees;
					*tree = *tree * -1;
					
					if(!*tree)
					{
						*tree = -1;
					}
				}
				
				last_blocking_tree_x = xleft;
			}
			
			if(tree_height == 9)
			{
				break;
			}
		}
		
		tree_height = -1;
		
		for(unsigned xright = 99 - 1; xright > last_blocking_tree_x; --xright)
		{
			int * tree = get(y, xright);
			int treeh = std::abs(*tree);
			
			if(treeh > tree_height)
			{
				tree_height = treeh;
				
				if(*tree >= 0)
				{
					++visible_trees;
					*tree = *tree * -1;
					
					if(!*tree)
					{
						*tree = -1;
					}
				}
			}
			
			if(tree_height == 9)
			{
				break;
			}
		}
	}
	
	return visible_trees;
}

void part1()
{
	auto horisontally = find_trees([](unsigned y, unsigned x) -> int * {
		return &(grid[y][x]);
	});
	
	auto vertically = find_trees([](unsigned y, unsigned x) -> int * {
		return &(grid[x][y]);
	});
	
	std::cout << (vertically + horisontally) << "\n";
}

void part2()
{
	unsigned scenic_score = 0;
	
	for(unsigned y = 1; y < 99-1; ++y)
	{
		for(unsigned x = 2; x < 99-1; ++x)
		{
			int tree = std::abs(grid[y][x]);
			unsigned left = 0;
			
			for(unsigned xi = x - 1; xi < 99; --xi)
			{
				int next_tree = std::abs(grid[y][xi]);
				++left;
				
				if(next_tree >= tree)
				{
					break;
				}
			}
			
			unsigned right = 0;
			
			for(unsigned xi = x + 1; xi < 99; ++xi)
			{
				int next_tree = std::abs(grid[y][xi]);
				++right;
				
				if(next_tree >= tree)
				{
					break;
				}
			}
			
			unsigned top = 0;
			
			for(unsigned yi = y - 1; yi < 99; --yi)
			{
				int next_tree = std::abs(grid[yi][x]);
				++top;
				
				if(next_tree >= tree)
				{
					break;
				}
			}
			
			unsigned bottom = 0;
			
			for(unsigned yi = y + 1; yi < 99; ++yi)
			{
				int next_tree = std::abs(grid[yi][x]);
				++bottom;
				
				if(next_tree >= tree)
				{
					break;
				}
			}
			
			scenic_score = std::max(scenic_score, left * right * bottom * top);
		}
	}
	
	std::cout << scenic_score << "\n";
}

