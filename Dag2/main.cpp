void part2()
{
	long points = 0;
	
	char const * ptr = input;
	
	while(*ptr != '.')
	{
		long opponents = (unsigned char)*ptr++;
		long mine = (unsigned char)*ptr++;
		
		if(mine == 0)
		{
			// do lose
			mine = (3 + opponents - 1) % 3;
			points += (mine + 1 + 0);
		}
		else if(mine == 1)
		{
			// do draw
			mine = opponents;
			points += (mine + 1 + 3);
		}
		else
		{
			// do win
			mine = (3 + opponents + 1) % 3;
			points += (mine + 1 + 6);
		}
	}
	
	std::cout << points << std::endl;
}

